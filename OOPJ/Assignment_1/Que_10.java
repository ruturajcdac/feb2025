/* 10. Write a program to compare two integers using all 
relational operators (==, !=, >, <, >=, <=) and display the results.*/
   
 class Que_10{
	public static void main(String args[]){
	
	int a = 10;
	int b = 54;
	
	System.out.println(a + " == " + b + " is " + (a==b));
	System.out.println(a + " != " + b + " is " + (a!=b));
	System.out.println(a + " > " + b + " is " +  (a>b));
	System.out.println(a + " < " + b + " is " +  (a<b));
	System.out.println(a + " >= " + b + " is " +  (a>=b));
	System.out.println(a + " <= " + b + " is " +  (a<=b));
	
	
	
	
		
		
	}
}