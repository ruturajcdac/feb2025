/* 8. Write a program to perform addition, subtraction, multiplication, division, and modulus operations on two integer 
   numbers and display the results.*/
   
 class Que_8{
	public static void main(String args[]){
	
	int a = 10;
	int b = 54;
	
	System.out.println("Sum: "+ a + " + " + b + " = " + (a+b));
	System.out.println("Difference: "+ a + " - " + b + " = " + + (a-b));
	System.out.println("Product: "+ a + " * " + b + " = " +  (a*b));
	System.out.printf("Division: %d / %d = %.4f \n", a, b, ((float)a/(float)b));
	System.out.println("Modulus: "+ a + " % " + b + " = " +  (a%b));
	
	
	
		
		
	}
}