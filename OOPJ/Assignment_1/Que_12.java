//12. Write a program to demonstrate the use of assignment operators (=, +=, -=, *=, /=, %=) on two integers.
import java.util.*;

class Que_12{
	public static void main(String args[]){
	
	int a = 10;
	System.out.println("a :" + a);
	
	System.out.println("a += : (a+=1): " + (a+=1));
	System.out.println("a -= : (a-=3): " + (a-=3));
	System.out.println("a *= : (a*=5): " + (a*=5));
	System.out.println("a /= : (a/=4): " + (a/=4));
	System.out.println("a %= : (a%=10): " + (a%=10));
	
	
			
	}
}